public class EnvSettingEntrance {
    String ip;
    String port;
    String dbName;
    String userName;
    String password;

    public EnvSettingEntrance(String ip, String port, String dbName, String userName, String password){
        this.ip = ip;
        this.port = port;
        this.dbName = dbName;
        this.userName = userName;
        this.password = password;
    }
}
